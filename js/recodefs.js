								+" id='"+_nodes[j].id+"' "
								+ " cx='"+ (_nodes[j].x)+"' "
								+ " cy='"+ (_nodes[j].y)+"' "
								+ " rx='"+ (_nodes[j].rx)+"' "
								+ " ry='"+ (_nodes[j].ry)+"' "
								+ " fill='"+defaultFillCOLOR+"' " 
								+ " stroke='"+_nodes[j].color+"' " 
								+ " stroke-dasharray='"+tempstrokedotted+"' " 
								+ " stroke-width='"+_nodes[j].strokewidth+"px' " 
								+ " ></ellipse>";
